import random
WORDS=('ключ','пример','нота','путь','линия')
word= random.choice(WORDS)
o_o= '-'*len(word)
wrong= 0
used= []
print ('\t\t\t\tДобро пожаловать в игру "Угадай слово"\n')
print ('Сейчас компьютер загадает какое-либо слово и назовет количество букв в нем. У Вас есть 5 попыток, что бы отгадать буквы этого слова, а затем Вы должны назвать слово целиком.\n')
print ('Удачи!!!\n')
while wrong<5 and o_o !=word:
       print ('\nВ этом слове',len(word),'букв:',o_o)
       print ('Вы уже предлагали эти буквы:',used)
       guess= input ('\nЕсть ли эта буква в слове?(Введите букву):')
       guess= guess.lower() 
       while guess in used:
           print('Вы уже предлагали  эту букву:',guess)
           print ('\nВы уже предлагали эти буквы:',used)
           guess= input ('\nЕсть ли эта буква в слове?(Введите букву):')
           guess= guess.lower()
       used.append(guess)
       if guess in word:
           print('Да.')
           new= ''
           for i in range(len(word)):
               if guess == word[i]:
                   new += guess
               else:
                   new += o_o[i]
           o_o= new
       else:
           print('Нет.')
       wrong +=1
print ('\nВ этом слове',len(word),'букв:',o_o)
print ('Вы уже предлагали эти буквы:',used)
user =input ('\nПопытки закончились! Теперь попробуйте угадать целое слово:')
if user == word:
       print ('\nВерно! Это слово',word,'!')
else:
       print ('\nК сожалению у Вас не получилось отгадать слово. ')
